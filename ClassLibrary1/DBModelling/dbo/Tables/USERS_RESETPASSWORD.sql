CREATE TABLE [dbo].[USERS_RESETPASSWORD]
(
	[CODIGO] INT NOT NULL PRIMARY KEY IDENTITY, 
    [CLIENTEID] INT NOT NULL, 
    [USUARIOID] INT NULL, 
    [DATA] DATETIME NOT NULL, 
    [TOKEN] VARCHAR(36) NOT NULL, 
    [SENHATROCADA] BIT NOT NULL, 
