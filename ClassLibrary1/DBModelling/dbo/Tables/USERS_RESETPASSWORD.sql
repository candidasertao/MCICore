	CONSTRAINT [FK_USERS_RESETPASSWORD_CLIENTES] FOREIGN KEY (CLIENTEID) REFERENCES dbo.CLIENTES(CLIENTEID) ON DELETE CASCADE, 
)
GO
CREATE  NONCLUSTERED INDEX [IX_USERS_RESETPASSWORD_TOKENUNIQUE] ON dbo.USERS_RESETPASSWORD(TOKEN)